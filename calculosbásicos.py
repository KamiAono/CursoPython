n1 = int(input('digite um numero'))
n2 = int(input('digite outro numero'))
s = n1+n2
sub = n1-n2
m = n1 * n2
d = n1 / n2
p = n1 ** n2
di = n1 // n2
print(' a soma é {}, a subtração é {}, o produto é {}, a divisão é {}'.format(s, sub, m, d), end='')
print (' a potencialização é{}, a divisão inteira é{}'.format(p, di))